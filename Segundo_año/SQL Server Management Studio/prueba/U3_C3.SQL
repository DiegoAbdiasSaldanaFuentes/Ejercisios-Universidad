--RESOLVER LAS SIGUIENTES CONSULTAS

--MOSTRAR TODOS LOS LIBROS SIEMPRE Y CUANDO LA EDITORIAL SEA SUPERIOR A 3
SELECT *
FROM CACB_LIBROS
WHERE (COD_EDITORIAL > 3);

--MOSTRAR LOS PRESTAMOS QUE SE HAYAN REALIZADO ANTES DEL 17/06 
--(DEBE CONSIDERAR QUE UNA FECHA SE INSERTA IGUAL QUE UN VARCHAR PERO SIEGUIENDO
--LA SIGUIENTE LÓGICA: 'MM/DD/AAAA'
SELECT *
FROM CACB_PRESTAMOS
WHERE (FECHA_PRESTAMO < '06/17/2025');

--MOSTRAR LOS PRESTAMOS QUE HAYAN ENTREGADO UN EJEMPLAR 3
SELECT * 
FROM CACB_DETALLE_PRESTAMO_EJEMPLAR
WHERE (COD_EJEMPLAR = 3);


-- PILLERÍAS DEL PROFE =)

--MOSTRAR LOS PRESTAMOS QUE HAYAN ENTREGADO UN EJEMPLAR 3, CONSIDERANDO EL SIGUIENTE
-- REPORTE: COD_PRESTAMO, COD_EJEMPLAR, TITULO LIBRO Y FECHA_PRESTAMO
SELECT P.COD_PRESTAMO, E.COD_EJEMPLAR, L.TITULO_LIBRO, P.FECHA_PRESTAMO
FROM CACB_PRESTAMO P, CACB_LIBRO L, CACB_DETALLE_PRESTAMO_EJEMPLAR DPE, CACB_EJEMPLAR E
WHERE (COD_EJEMPLAR = 3) AND (P.COD_PRESTAMO = DPE.COD_PRESTAMO)
	AND(DPE.COD_EJEMPLAR=E.COD_EJEMPLAR)
	AND (E.COD_LIBRO = L.COD_LIBRO);



