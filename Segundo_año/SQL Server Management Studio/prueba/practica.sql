CREATE TABLE DS_ESTUDIANTES(
    RUT NUMBER,
    NOMBRE VARCHAR(50),
    EDAD NUMBER,
    DIRECCION VARCHAR(100),
    CONSTRAINT PK_RUT_DS_ESTUDIANTES PRIMARY KEY(RUT)
);

CREATE TABLE DS_CURSOS(
    COD_CURSO NUMBER,
    NOMBRE_CURSO VARCHAR(80),
    CANTIDAD_HORAS NUMBER,
    CONSTRAINT PK_COD_CURSO_DS_CURSOS PRIMARY KEY(COD_CURSO)
);

--zona 2--

CREATE TABLE DS_INSCRIPCIONES(
    RUT_ESTUDIANTE NUMBER,
    COD_CURSO NUMBER,
    FECHA_INSCRIPCION DATE,
    CONSTRAINT FK_RUT_EST_DS_INS FOREIGN KEY(RUT_ESTUDIANTE) REFERENCES  DS_ESTUDIANTES(RUT),
    CONSTRAINT FK_COD_CURS_DS_INS FOREIGN KEY(COD_CURSO) REFERENCES DS_CURSOS(COD_CURSO)
);

