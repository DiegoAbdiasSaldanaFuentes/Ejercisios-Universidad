let a = 10;
let b = 3;

// Aritméticos
console.log(a + b); // suma
console.log(a - b); // resta
console.log(a * b); // multiplicación
console.log(a / b); // división
console.log(a % b); // módulo (resto)
console.log(a ** b); // potencia

// Comparación
console.log(a > b);  // mayor
console.log(a < b);  // menor
console.log(a == b); // igualdad débil
console.log(a === b); // igualdad estricta
console.log(a != b); // distinto

// Lógicos
console.log(a > 5 && b < 5); // AND
console.log(a > 5 || b > 10); // OR
console.log(!(a > b)); // NOT
